import UIKit
import SwiftUI


extension UIColor {
    
    static func emailButtonColor() -> UIColor {
        return UIColor(red: 97/255, green: 97/255, blue: 97/255, alpha: 1)
    }
    
    static func loginButtonColor() -> UIColor {
        return UIColor(red: 85/255, green: 230/255, blue: 226/255, alpha: 1)
    }
    
    
    
}
