import UIKit

extension UIFont {
    
    static func galvji20() -> UIFont? {
        return UIFont(name: "Galvji", size: 20)
    }
    
    static func galvji30() -> UIFont? {
        return UIFont(name: "Galvji", size: 30)
    }
}
